     */
    public function store(Request $request)
    {
        //
        $this->validate($request, [
            'name'=>'required|max:255'
        ]);
        $tag = new Tags;
        $tag->name =  $request->input('name');
        $tag->save();


        return redirect()->route('tags.index')->with('success','Tag Created');

    }

